/**
 * Traduce una key al idioma indicado.
 *
 * Cascada de fallback cuando la traducción no existe:
 *   locale → EN → ES → la key cruda.
 *
 * Esto significa que un idioma con cobertura parcial (p. ej. FR con 60%
 * de las keys traducidas) mostrará el resto en inglés en vez de saltar
 * a español, lo cual es más coherente para audiencias europeas.
 */
export function t(key: keyof typeof DICTIONARY | string, locale: Locale = DEFAULT_LOCALE): string {
  const entry = DICTIONARY[key];
  if (!entry) return key;
  return entry[locale] ?? entry.en ?? entry.es ?? key;
}

/** Helper: devuelve una función `t` ya bindeada al locale. Útil server-side. */
export function tBinder(locale: Locale): (k: string) => string {
  return (k) => t(k, locale);
}
